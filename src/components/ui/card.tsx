import * as React from 'react'
import { cn } from '@/lib/utils'

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { hoverable?: boolean }
>(({ className, hoverable = false, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      'bg-white rounded-xl border border-slate-200 shadow-(--shadow-card)',
      hoverable &&
        'cursor-pointer transition-all duration-200 hover:border-slate-300 hover:shadow-(--shadow-card-hover)',
      className
    )}
    {...props}
  />
))
Card.displayName = 'Card'

